            <h5>Polícia Federal atualmente</h5>
            <p>
                Hoje, a Polícia Federal é responsável por diversas funções, como:
                <ul>
                    <li>
                        Investigar crimes contra a União (corrupção, tráfico internacional, crimes ambientais).
                    </li>
                    <li>
                        Controlar fronteiras e aeroportos.
                    </li>
                    <li>
                        Combater crimes cibernéticos e financeiros.
                    </li>
                    <li>
                        Emitir passaportes e fiscalizar imigração.  
                    </li>
                    <li>
                        Atuar em operações internacionais e cooperação com Interpol.
                    </li>
                    <li>
                        A Polícia Federal continua a evoluir e desempenha um papel fundamental na segurança e justiça do Brasil.
                    </li>
                </ul>
            </p>
